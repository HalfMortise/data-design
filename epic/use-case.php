		<h1>Use Case</h1>
			<p class="container">Nate wants to be able to access Netflix and see content that is relevant to him and not those that he shares his account with (his roommates).  While his roommates tend to watch a lot of cartoons and kid shows, he enjoys the Sci-Fi/dystopian/post-apocalyptic genres, and would prefer not to see his page littered with recommendations for irrelevant content.</p>
		<h1>Interaction Flow</h1>
		<h4>Using Netflix Tracks (Indv. Profiles) From Login to Adding Content to Track</h4>
		<ul>
			<li class="container">User logs into Netflix account with username and password</li>
			<li class="container">User is logged in, then prompted to select a profile to enter under</li>
			<li class="container">User selects profile</li>
			<li class="container">User is directed to page with content marketed directly to that individual user</li>
			<li class="container">User scrolls through content and sees a show he likes</li>
			<li class="container">User rates the show by clicking “thumbs up”</li>
			<li class="container">Content is flagged as relevant to user's track</li>
			<li class="container">User continues to scroll through content and sees a movie to watch later</li>
			<li class="container">User adds content to My List by clicking +</li>
		</ul>

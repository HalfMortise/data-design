		<h1>Use Case</h1>
			<p class="container">Nate wants to be able to access Netflix and see content that is relevant to him and not those that he shares his account with (his roommates).  He also wants to be able to resume episodes of his favorite TV shows where he left off when he had previously been viewing, without having to remember at which point he was in the episode.</p>
		<h1>Interaction Flow</h1>
		<h4>Using Netflix Tracks (Indv. Profiles) From Login to Adding Content to Track</h4>
		<ul>
			<li class="container">User logs into Netflix account with username and password</li>
			<li class="container">User is logged in, then prompted to select a profile to enter under</li>
			<li class="container">User selects profile</li>
			<li class="container">User is directed to page which contains previously viewed content</li>
			<li class="container">User scrolls through content and finds the show he had been watching</li>
			<li class="container">User hovers his cursor over the show and clicks the triangular ‘Play’ button</li>
			<li class="container">Content expands to fill window and begins playing</li>
			<li class="container">Netflix tracks progress throughout user viewing</li>
		</ul>
